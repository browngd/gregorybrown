                  <a name="process"><h2>The Process</h2></a>
                  <p>BitCrunched! understands that as a business owner you have several responsibilities.  You can rest assured that we understand your dedication and commitment and want to contribute to your success.  This is why BitCrunched! makes the process of creating a website as easy as possible for you.</p><br />
                  <h4>The Discovery Phase</h4>
                  <p>BitCrunched! starts with a discovery phase to determine all of the project requirements and the initial expectations of the client.  You are in the driver's seat and through this process BitCrunched! learns about the goals of the project.  The most important aspect of this phase is listening to the client to determine the direction for the project.</p><br />
                  <h4>The Design Phase</h4>
                  <p>This is the process where all of the fun happens.  This process starts by determining the look and feel of the site.  The client is presented with multiple "mock-ups" based on the Discovery Phase.  These might include completely different layouts or color schemes or could be small variations on a common theme.  As a client, you choose what you like and what you don't like.  After we have determined the layout of the site, we will then work together to generate the content for the site.  After this has been completed, BitCrunched! will submit a final design for review.  After the client has approved the final design, we will move to the final phase.</p><br />
                  <h4>The Deployment Phase</h4>
                  <p>This is the final phase and often the quickest phase.  During this phase we will finalize any last minute changes and move forward with the deployment of the site onto the production hosting servers.  Before deployment all code and images will be optimized.  The site will be analized for Search Engine Optimization and Accesibility.  At this point, you can be proud of what we have worked together to build.</p>
